export async function getProgramacion(): Promise<Programa[]> {
  return Promise.resolve(programas);
}

export async function getProgramasPorDia(dia: string): Promise<Programa[]> {
  return Promise.resolve(programas.filter(p => p.dias.includes(dia)));
}

export async function getProgramasPorCategoria(categoria: string): Promise<Programa[]> {
  return Promise.resolve(programas.filter(p => p.categoria === categoria));
}

// Nueva función para agrupar programas por nombre
export async function getProgramasAgrupados(): Promise<Programa[]> {
  const programasUnicos = new Map<string, Programa>();
  
  programas.forEach((programa) => {
    const key = `${programa.nombre}-${programa.horario}`;
    
    if (programasUnicos.has(key)) {
      // Si ya existe, agregar los días
      const existing = programasUnicos.get(key)!;
      const diasCombinados = [...new Set([...existing.dias, ...programa.dias])];
      programasUnicos.set(key, {
        ...existing,
        dias: diasCombinados.sort((a, b) => {
          const orden = ['L', 'M', 'X', 'J', 'V', 'S', 'D'];
          return orden.indexOf(a) - orden.indexOf(b);
        }),
      });
    } else {
      // Si no existe, agregarlo
      programasUnicos.set(key, { ...programa });
    }
  });
  
  return Array.from(programasUnicos.values());
}
